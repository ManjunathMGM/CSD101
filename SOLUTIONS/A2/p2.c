#include <stdio.h>
int main() // ~ basic program to check wheather the number is positive, negative or zero.
{

    int a;
    scanf("%d", &a);

    if (a > 0)
        printf("A IS POSITIVE");
    else if (a < 0)
        printf("A IS NEGATIVE");
    else
        printf("A IS EQUAL TO ZERO");

    return 0;
}
